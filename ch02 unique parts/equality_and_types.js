/*Variable Types*/

var is20 = false;
console.log(`${is20} is ${typeof is20}`);

var age = 19;
console.log(`${age} is ${typeof age}`);

var lastName = "bae";
console.log(`${lastName} is ${typeof lastName}`);

var fruits = ["apple", "banana", "kiwi"];
console.log(`${fruits} is ${typeof fruits}`);

var me = {firstName: "John", lastName: "Smith"};
console.log(`${me} is ${typeof me}`);

var nullVar = null;
console.log(`${nullVar} is ${typeof nullVar}`);

var function1 = function(){
    console.log(1);
}
console.log(`${function1} is ${typeof function1}`);

var blank;
console.log(`${blank} is ${typeof blank}`);
